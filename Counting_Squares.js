const squareCount = (a,b) => {
    let c = 0 ;  // our counter start at 0 
    for(let f=a;f<=b;f++){     // O(N) time complexity
        Number.isInteger(Math.sqrt(f))&&c++; //check if f is a perfect square
    }
    return c
}



